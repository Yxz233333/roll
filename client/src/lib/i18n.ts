export const getTranslation = (lang: Language, key: keyof typeof translations.en, replacements?: Record<string, string | number>) => {
  let text = translations[lang][key] || translations.en[key] || key;
  
  if (replacements) {
    Object.entries(replacements).forEach(([key, value]) => {
      text = text.replace(`{${key}}`, String(value));
    });
  }
  
  return text;
};
